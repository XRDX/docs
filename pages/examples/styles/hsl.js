/***************************************************************

颜色HSL表示法

HSL(h, saturation, light)

* h: 颜色编号，从0到360
* saturation：饱和度，0~1
* light：亮度，0~1

***************************************************************/

rectangle(100, 100, 80, 50, HSL(100, 0.5, 0.5));
